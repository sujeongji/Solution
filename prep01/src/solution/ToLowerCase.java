package solution;

public class ToLowerCase {
    public String toLowerCase(String str) {
        return str.toLowerCase();
    }


	public static void main(String args[]) {
	    String str = "HELLO";
	    String strlower = str.toLowerCase();
	    System.out.println(strlower); 
 }
}

